#!/bin/bash

VERSION_STRING=(`grep ELECTRUM_VERSION lib/version.py`)
ELECTRUM_AXE_VERSION=${VERSION_STRING[2]}
ELECTRUM_AXE_VERSION=${ELECTRUM_AXE_VERSION#\'}
ELECTRUM_AXE_VERSION=${ELECTRUM_AXE_VERSION%\'}
DOTS=`echo $ELECTRUM_AXE_VERSION |  grep -o "\." | wc -l`
if [[ $DOTS -lt 3 ]]; then
    ELECTRUM_AXE_APK_VERSION=$ELECTRUM_AXE_VERSION.0
else
    ELECTRUM_AXE_APK_VERSION=$ELECTRUM_AXE_VERSION
fi
export ELECTRUM_AXE_VERSION
export ELECTRUM_AXE_APK_VERSION
